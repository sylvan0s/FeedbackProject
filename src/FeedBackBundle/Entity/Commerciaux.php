<?php

namespace FeedBackBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Commerciaux
 *
 * @ORM\Table(name="commerciaux")
 * @ORM\Entity(repositoryClass="FeedBackBundle\Repository\CommerciauxRepository")
 */
class Commerciaux
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var int
     *
     * @ORM\Column(name="id_user", type="integer", unique=true)
     */
    private $idUser;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set idUser
     *
     * @param integer $idUser
     *
     * @return Commerciaux
     */
    public function setIdUser($idUser)
    {
        $this->idUser = $idUser;

        return $this;
    }

    /**
     * Get idUser
     *
     * @return int
     */
    public function getIdUser()
    {
        return $this->idUser;
    }
}

